package ch.hslu.plab.sw06.Calculator.Interfaces;

/**
 * Interface which provides the functionality to add two integers.
 */
public interface IntegerCalculator {

    /**
     * Adds two numbers and returns its result.
     * @param firstNumber The first number to add.
     * @param secondNumber The second number to add.
     * @return The result of the addition.
     */
    int add(int firstNumber, int secondNumber);
}
